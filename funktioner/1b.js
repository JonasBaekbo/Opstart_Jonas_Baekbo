var result;
function plus(tal1, tal2) {
    result = tal1 + tal2;
    console.log(result);
}
function minus(tal1, tal2) {
    result = tal1 - tal2;
    console.log(result);
}
function gange(tal1, tal2) {
    result = tal1 * tal2;
    console.log(result);
}
function divider(tal1, tal2) {
    result = tal1 / tal2;
    console.log(result);
}
function kvadratrod(tal1) {
    result = Math.sqrt(tal1);
    console.log(result);
}
function udregning_af_pi(tal1) {
    result = Math.PI + tal1;
    console.log(result);
}
plus(10, 20);
minus(50, 25);
gange(5, 5);
divider(50, 5);
kvadratrod(25);
udregning_af_pi(10);
