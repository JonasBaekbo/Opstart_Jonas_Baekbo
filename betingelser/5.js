var laeskedrik = "Faxe Kondi";
switch (laeskedrik) {
    case "Faxe Kondi":
        console.log('Jeg kan godt lide Faxe Kondi')
        break;
    case "Coca Cola":
        console.log('Jeg kan godt lide Coca Cola')
        break;
    case "Fanta":
        console.log('Jeg kan godt lide Fanta')
        break;
    default:
        console.log('Jeg kan godt lide noget andet end Faxe Kondi, Coca Cola og Fanta')
        break;
}